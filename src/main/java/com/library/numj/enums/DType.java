package com.library.numj.enums;

/**
 * Enumeration representing different data types supported in NumJ.
 */
public enum DType {
	/** 32-bit floating point number. */
	FLOAT32,
	/** 64-bit floating point number. */
	FLOAT64,
	/** 8-bit integer. */
	INT8,
	/** 16-bit integer. */
	INT16,
	/** 32-bit integer. */
	INT32,
	/** 64-bit integer. */
	INT64
}
